    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        cari = fieldCari.getText();
        int row = tabelPaket.getRowCount();
        for(int a=0;a<row;a++){
        model.removeRow(0);
        }
        try {
            sql ="SELECT * FROM paket WHERE nama_paket like'"+cari+"%' OR harga like'"+cari+"%' OR deskripsi like'"+cari+"%'";
            stm = con.createStatement();
            r = stm.executeQuery(sql);
            while(r.next()){
            String data[]={r.getString(1),r.getString(2),r.getString(3),r.getString(4)};
            model.addRow(data);
            }
            stm.close();
            r.close();
        } catch (Exception e) {
            System.out.println("Error"+e.getMessage());
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnCariPembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariPembayaranActionPerformed
        cari = fieldCariPembayaran.getText();
        int row = tabelPembayaran.getRowCount();
        for(int a=0;a<row;a++){
        modelPembayaran.removeRow(0);
        }
        
        try {
            sql ="SELECT rsv.id_reservasi, rsv.nama, rsv.tanggal_pergi, rsv.tanggal_pulang,pkt.harga, rsv.status "
                    + "FROM reservasi AS rsv INNER JOIN paket AS pkt ON rsv.id_paket=pkt.id_paket WHERE  nama like'"+cari+"%' OR tanggal_pergi like'"+cari+"%' OR tanggal_pulang like'"+cari+"%' OR status like'"+cari+"%'";
//             WHERE = rsv.id_costumer='"+idCostumer+"'
            stm = con.createStatement();
            r = stm.executeQuery(sql);
            while(r.next()){
            String dataPembayaran[]={r.getString(1),r.getString(2),r.getString(3),r.getString(4),r.getString(5),r.getString(6)};
            modelPembayaran.addRow(dataPembayaran);
            }
            stm.close();
            r.close();
        } catch (Exception e) {
            System.out.println("Error"+e.getMessage());
        }
    }//GEN-LAST:event_btnCariPembayaranActionPerformed

