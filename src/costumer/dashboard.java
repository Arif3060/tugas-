    private void tampilkan() {
        int row = tabelPaket.getRowCount();
        for(int a=0;a<row;a++){
        model.removeRow(0);
        }
        try {
            sql ="SELECT * FROM paket";
            stm = con.createStatement();
            r = stm.executeQuery(sql);
            while(r.next()){
            String data[]={r.getString(1),r.getString(2),r.getString(3),r.getString(4)};
            model.addRow(data);
            }
            stm.close();
            r.close();
        } catch (Exception e) {
            System.out.println("Error"+e.getMessage());
        }
    }
    
    private void tampilkanPembayaran(){
        int row = tabelPembayaran.getRowCount();
        for(int a=0;a<row;a++){
        modelPembayaran.removeRow(0);
        }
        
        try {
            idCostumer = tID.getText();
            sql ="SELECT rsv.id_reservasi, rsv.nama, rsv.tanggal_pergi, rsv.tanggal_pulang,pkt.harga, rsv.status "
                    + "FROM reservasi AS rsv INNER JOIN paket AS pkt ON rsv.id_paket=pkt.id_paket WHERE rsv.id_costumer='"+idCostumer+"'";
             
            stm = con.createStatement();
            r = stm.executeQuery(sql);
            while(r.next()){
            String dataPembayaran[]={r.getString(1),r.getString(2),r.getString(3),r.getString(4),r.getString(5),r.getString(6)};
            modelPembayaran.addRow(dataPembayaran);
            }
            stm.close();
            r.close();
        } catch (Exception e) {
            System.out.println("Error"+e.getMessage());
        }
    }
    
